      SUBROUTINE DSAUPD_C(IDO,N,NEV,TOL,RESID,NCV,V,LDV,IPARAM,IPNTR,
     &WORKD,WORKL,LWORKL,INFO)
     &BIND(C,NAME="dsaupd_c")
      USE :: ISO_C_BINDING
C
      IMPLICIT NONE
      INTEGER(KIND=C_INT32_T),INTENT(INOUT) :: IDO
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: N
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: NEV
      REAL(KIND=C_DOUBLE),INTENT(IN) :: TOL
      REAL(KIND=C_DOUBLE),INTENT(INOUT) :: RESID
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: NCV
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: V
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: LDV
      INTEGER(KIND=C_INT32_T),INTENT(INOUT) :: IPARAM
      INTEGER(KIND=C_INT32_T),INTENT(OUT) :: IPNTR
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: WORKD
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: WORKL
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: LWORKL
      INTEGER(KIND=C_INT32_T),INTENT(INOUT) :: INFO
C
      DIMENSION RESID(N),V(N*NCV),IPARAM(11),IPNTR(11),WORKD(3*N),
     &WORKL(LWORKL)
C
C     WE NEED 'LM' (USE SHIFT-INVERT MODE), BECAUSE 'SM' IS TOO SLOW
      CALL DSAUPD(IDO,'G',N,'LM',NEV,TOL,RESID,NCV,V,LDV,IPARAM,IPNTR,
     &WORKD,WORKL,LWORKL,INFO)
C
      END SUBROUTINE DSAUPD_C
C
CCC DSEUPD
      SUBROUTINE DSEUPD_C(SELECT,D,Z,LDZ,N,NEV,TOL,RESID,NCV,V,LDV,
     &SIGMA,IPARAM,IPNTR,WORKD,WORKL,LWORKL,INFO)
     &BIND(C,NAME="dseupd_c")
      USE :: ISO_C_BINDING
C
      LOGICAL(KIND=C_DOUBLE),INTENT(IN) :: SELECT
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: D
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: Z
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: LDZ
      INTEGER(KIND=C_DOUBLE),INTENT(IN) :: SIGMA
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: N
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: NEV
      REAL(KIND=C_DOUBLE),INTENT(IN) :: TOL
      REAL(KIND=C_DOUBLE),INTENT(INOUT) :: RESID
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: NCV
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: V
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: LDV
      INTEGER(KIND=C_INT32_T),INTENT(INOUT) :: IPARAM
      INTEGER(KIND=C_INT32_T),INTENT(OUT) :: IPNTR
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: WORKD
      REAL(KIND=C_DOUBLE),INTENT(OUT) :: WORKL
      INTEGER(KIND=C_INT32_T),INTENT(IN) :: LWORKL
      INTEGER(KIND=C_INT32_T),INTENT(INOUT) :: INFO
C
      DIMENSION SELECT(NCV),D(NEV),Z(N*NEV),RESID(N),V(N*NCV),
     &IPARAM(11),IPNTR(11),WORKD(3*N),WORKL(LWORKL)
C
C     WE NEED 'LM' (USE SHIFT-INVERT MODE), BECAUSE 'SM' IS TOO SLOW
      CALL DSEUPD(.TRUE.,'A',SELECT,D,Z,LDZ,SIGMA,'G',N,'LM',NEV,TOL,
     &RESID,NCV,V,LDV,IPARAM,IPNTR,WORKD,WORKL,LWORKL,INFO)
C
      END SUBROUTINE DSEUPD_C
